public class TreeNode {
    public int val;
    public TreeNode left;
    public TreeNode mid;
    public TreeNode right;
    public TreeNode(int val=0, TreeNode left=null, TreeNode mid = null, TreeNode right = null) {
        this.val = val;
        this.left = left;
        this.mid = mid;
        this.right = right;
    }
}
